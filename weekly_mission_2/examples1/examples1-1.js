// Ejemplo 1: for Each para imprimir elementos de una lista
const numbers = [1, 2, 3, 4, 5];
console.log("Ejemplo 1: Imprimiendo los elementos de una lista")
numbers.forEach(num => console.log(num)) //lo que hace basicamente es crear un ciclo por cada elemento que se encunetre en la lista, razon por la cual se llama forEach
//num es una variable creada solo para el ciclo el cual se cambiara su valor por cada elemento dentro de la lista